    /**
     * Displays homepage.
     *
     * @return mixed
     */
    public function actionIndex()
    {
        $quizes = null;
        if(Yii::$app->user->id){
            $quizes = Quizes::getActiveQuizesForUser(Yii::$app->user->id);
            return $this->render('index', [
                'quizes' => $quizes
            ]);
        }else{
            $now = new \DateTime();
            $quizes = Quizes::find()->where(['quizes.status' => 'open'])
                ->andWhere(['<', 'quizes.start_at', $now->getTimestamp()])
                ->andWhere(['>', 'quizes.end_at', $now->getTimestamp()])
                ->orderBy('created_at DESC')->all();
            return $this->render('index', [
                'quizes' => $quizes
            ]);
        }
    }
    
    public function actionHistory()
    {
        if(Yii::$app->user->isGuest){
            return $this->redirect(['/']);
        }
        return $this->render('history', [
