    /**
     * Displays contact page.
     *
     * @return mixed
     */
    public function actionContact()
    {
        $model = new ContactForm();
        if ($model->load(Yii::$app->request->post()) && $model->validate()) {
            if(is_null(Settings::findOne(['key'=>'adminEmail'])))
            {
                Yii::$app->session->setFlash('error', 'Ошибка при отправке сообщения.');
                return $this->refresh();
            }
            if ($model->sendEmail(Settings::findOne(['key'=>'adminEmail'])->value)) {
                Yii::$app->session->setFlash('success', 'Спасибо вам за ваш вопрос. Мы ответим вам как можно скорее.');
            } else {
                Yii::$app->session->setFlash('error', 'Ошибка при отправке сообщения.');
            }

            return $this->refresh();
        } else {
            return $this->render('contact', [
                'model' => $model,
            ]);
        }
    }

